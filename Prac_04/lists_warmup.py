numbers[-1] # takes the last element
numbers[:-1] # displays all elements
numbers[3:4] # displays 3 and 4 which is 1,5
5 in numbers # check if 5 is in the numbers
7 in numbers # check if 7 is in the numbers
"3" in numbers # check to see if 3 is in the string
numbers + [6, 5, 3] # adds 6, 5 and 3 to the end of the number


